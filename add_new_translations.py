# For locales without dedicated translations, use English fallback labels
# (Android will automatically use values/strings.xml as fallback,
#  so we only need to write files for locales that HAVE entries above)

def escape_xml(text):
    return text.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;").replace("'", "\\'")

for lang, strings in TRANSLATIONS.items():
    lang_dir = os.path.join(RES_DIR, f"values-{lang}")
    os.makedirs(lang_dir, exist_ok=True)
    path = os.path.join(lang_dir, "strings.xml")

    # Read existing file if present
    if os.path.exists(path):
        with open(path, "r", encoding="utf-8") as f:
            content = f.read()
    else:
        content = '<?xml version="1.0" encoding="utf-8"?>\n<resources>\n</resources>'

    # Determine which keys are missing
    new_lines = []
    for key, val in strings.items():
        if f'name="{key}"' not in content:
            new_lines.append(f'    <string name="{key}">{escape_xml(val)}</string>')

    if new_lines:
        insertion = "\n".join(new_lines) + "\n"
        content = content.replace("</resources>", insertion + "</resources>")
        with open(path, "w", encoding="utf-8") as f:
            f.write(content)
        print(f"  [{lang}] Added {len(new_lines)} keys")
    else:
        print(f"  [{lang}] All keys already present")

print("Done.")
